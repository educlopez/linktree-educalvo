interface Data {
  name: string;
  avatar: string;
  links: Link[];
  socials: Social[];
}

interface Link {
  href: string;
  title: string;
  image?: string;
}

interface Social {
  href: string;
  title: string;
}

export default async function HomePage() {
  const data: Data | undefined = await get("linktree");

  if (!data) {
    // not working yet https://github.com/vercel/next.js/issues/44232
    redirect("https://educalvolopez.com");
  }

  return (
    <div className="flex flex-col items-center justify-center w-full px-8 mx-auto mt-16">
      <Image
        priority
        className="rounded-full"
        alt={data.name}
        src={data.avatar}
        width={96}
        height={96}
      />
      <h1 className="mt-4 mb-8 text-xl font-bold text-white">{data.name}</h1>
      {data.links.map((link) => (
        <LinkCard key={link.href} {...link} />
      ))}
      <div className="flex items-center gap-4 mt-8 text-white">
        {data.socials.map((social) => (
          <a
            aria-label={`${social.title} link`}
            key={social.href}
            href={social.href}
            target="_blank"
            rel="noopener noreferrer"
          >
            {social.href.includes("twitter") ? (
              <TwitterIcon />
            ) : social.href.includes("github") ? (
              <GitHubIcon />
            ) : social.href.includes("instagram") ? (
              <InstagramIcon />
            ) : null}
          </a>
        ))}
      </div>
    </div>
  );
}
